package com.lsantamaria.demo.config;

import static org.springframework.web.reactive.function.server.RequestPredicates.GET;
import static org.springframework.web.reactive.function.server.RequestPredicates.POST;
import static org.springframework.web.reactive.function.server.RequestPredicates.path;
import static org.springframework.web.reactive.function.server.RouterFunctions.route;

import com.lsantamaria.demo.cars.CarsHandler;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.reactive.config.EnableWebFlux;
import org.springframework.web.reactive.config.WebFluxConfigurer;
import org.springframework.web.reactive.function.server.RouterFunction;
import org.springframework.web.reactive.function.server.RouterFunctions;
import org.springframework.web.reactive.function.server.ServerResponse;

@Configuration
@EnableWebFlux
class WebConfig implements WebFluxConfigurer {

  @Bean
  public RouterFunction<ServerResponse> routes(CarsHandler carsHandler){
    return RouterFunctions
        .nest(path("/cars"),
            route(GET("/"), carsHandler::fetchCars)
                .andRoute(POST("/"), carsHandler::addCar)
        );
  }
}
